package com.wysockil.rangescalculator;

public class Range {
    private final Integer begin;
    private final Integer end;

    public Range(Integer begin, Integer end) {
        this.begin = begin;
        this.end = end;
    }

    public Integer getBegin() {
        return begin;
    }

    public Integer getEnd() {
        return end;
    }


}
